    private void btn_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LoginActionPerformed
        // TODO add your handling code here:
        String name=txt_Name.getText();
        String email=txt_Email.getText();
        String user=txt_Username.getText();
        String pass=String.valueOf(txt_Password.getPassword());
        String repass=String.valueOf(txt_Repass.getPassword());
        
        if (txt_Name.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter a Name");
            txt_Name.grabFocus();
        }else
        
         if (txt_Email.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter an Email");
            txt_Email.grabFocus();
        } else
         
        if (txt_Username.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter a username");
            txt_Username.grabFocus();
        }else
        
        if (txt_Password.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter a Password");
            txt_Password.grabFocus();
        }else
        
        if (!pass.equals(repass)) {
            JOptionPane.showMessageDialog(rootPane, "Password do not match ");
            txt_Repass.grabFocus();
        }else{
       
         PreparedStatement pr;
         String query="INSERT INTO movieticket(name,mail,username,password) VALUES(?,?,?,?)";
         try {
            pr=Myconnection.getConnection().prepareStatement(query);
            
            pr.setString(1, name);
            pr.setString(2, email);
            pr.setString(3, user);
            pr.setString(4, pass);
            
             if (pr.executeUpdate() > 0) {
                
                  JOptionPane.showMessageDialog(rootPane, "New user added successfully");
             }
        } catch (SQLException e) {
             System.out.println("User not register");
        }
         Login_page lg=new Login_page();
         lg.setVisible(true);
         this.setVisible(false);
       }
       
    }//GEN-LAST:event_btn_LoginActionPerformed

