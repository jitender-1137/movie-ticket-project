     public boolean chckusername(String user){
         PreparedStatement ps;
         ResultSet rs;
         boolean checkuser=false;
        String query="SELECT * FROM `movieticket` WHERE `username` =?";
         try {
             ps=Myconnection.getConnection().prepareStatement(query);
             ps.setString(1, user);
             rs=ps.executeQuery();
             
             if (rs.next()) {
                 checkuser=true;
             }
             
         } catch (SQLException e) {
             System.out.println(e);
         }
         
         return checkuser;
   }    
